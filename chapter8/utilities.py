def is_stop_word(word: str):
  return word.lower() in stop_words

def load_features() -> [str]:
  features = []
  with open('chapter8/features.txt', 'r') as fp:
    for line in fp:
      features.append(line.strip())
  return features

def load_samples(features: [str]) -> [[], []]:
  cache = 'outputs/073_samples.pkl'

  # キャッシュがある
  if os.path.exists(cache):
    with open(cache, mode='rb') as fp:
      data = pickle.load(fp)
      return [data['X'], data['y']]

  X = [] # サンプルのOne-Hotベクトル
  y = [] # 正解ラベル

  with open('chapter8/sentiment.txt', 'r') as fp:
    for line in fp:
      ans, sentence = line.strip().split(' ', 1)
      y.append(1 if ans == '+1' else 0)
      X.append(sentence2vec(sentence, features))

  X = np.array(X)
  y = np.array(y)

  with open(cache, mode='wb') as fp:
    pickle.dump({'X': X, 'y': y}, fp)

  return [X, y]

def sentence2vec(sentence: str, features: []) -> []:
  n = len(features)
  vec = np.zeros(n)
  words = sentence.split(' ')
  for word in words:
    word = re.sub(r'[^a-z]', '', stem(word.lower()))
    if not word in features: continue
    index = features.index(word)
    vec[index] += 1
  return vec
